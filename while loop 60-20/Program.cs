
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace While_Loop_60_20
{
    class Program
    {
        static void Main(string[] args)
        {
            int i = 60;
            while (i > 20)
            {
                Console.WriteLine(i);
                i--;
            }
            Console.ReadLine();
        }
    }
}